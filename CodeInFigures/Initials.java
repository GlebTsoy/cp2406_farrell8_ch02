public class Initials {
    public static void main(String[] args){
        char firstInitial, secondInitial, thirdInitial;
        firstInitial = 'G';
        secondInitial = 'T';
        thirdInitial = 'P';
        System.out.print(firstInitial + "." + secondInitial + "." + thirdInitial + ".");
    }
}
